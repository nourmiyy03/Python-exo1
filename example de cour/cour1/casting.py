# Convertire vers un entier
nombre_texte = "123"
nombre_entier = int(nombre_texte)
print(nombre_entier + 7)  # Affiche: 130

# Convertire vers une chaîne
prix = 42.7
prix_str = str(prix)
print("Le prix est " + prix_str)  # Affiche: Le prix est 42.7

# Convertire Vers un décimal
valeur_lue = "9.99"
decimal = float(valeur_lue)
print(decimal)  # Affiche: 9.99